#ifndef CSR_H
#define CSR_H

enum MISA_MXL {
  BASE_INTEGER_32 = 1,
  BASE_INTEGER_64 = 2
};

enum MISA_EXTENSIONS {
  ATOMIC_EXTENSION = 0x0000001,
  B_EXTENSION = 0x0000002,
  COMPRESSED_EXTENSION = 0x0000004,
  DOUBLE_PRECISION_FLOATING_POINT_EXTENSION = 0x0000008,
  EMBEDDED_BASE_ISA = 0x0000010,
  SINGLE_PRECISION_FLOATING_PONT_EXTENSION = 0x0000020,
  HYPERVISOR_EXTENSION = 0x0000080,
  INTEGER_BASE_ISA = 0x0000100,
  INTEGER_MULTIPLY_DIVIDE_EXTENSION = 0x0001000,
  QUAD_PRECISION_FLOATING_POINT_EXTENSION = 0x0010000,
  SUPERVISOR_MODE_IMPLEMENTED = 0x0040000,
  USER_MODE_IMPLEMENTED = 0x0100000,
  VECTOR_EXTENSION = 0x0200000,
  NON_STANDARD_EXTENSIONS = 0x0800000
};

struct MISA {
  enum MISA_MXL mxl;
  enum MISA_EXTENSIONS extensions;
};

struct MISA get_misa();

#endif