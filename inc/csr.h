#ifndef CSR_H
#define CSR_H

#include <stdbool.h>

struct MISA {
  enum MACHINE_XLEN {
    MXL_BASE_INTEGER_32 = 1,
    MXL_BASE_INTEGER_64 = 2
  } machine_xlen;
  enum EXTENSIONS {
    A_EXTENSION = 0x0000001,
    B_EXTENSION = 0x0000002,
    C_EXTENSION = 0x0000004,
    D_EXTENSION = 0x0000008,
    EMBEDDED_BASE_ISA = 0x0000010,
    F_EXTENSION = 0x0000020,
    H_EXTENSION = 0x0000080,
    INTEGER_BASE_ISA = 0x0000100,
    M_EXTENSION = 0x0001000,
    Q_EXTENSION = 0x0010000,
    S_MODE_IMPLEMENTED = 0x0040000,
    U_MODE_IMPLEMENTED = 0x0100000,
    V_EXTENSION = 0x0200000,
    NON_STANDARD_EXTENSIONS = 0x0800000
  } extensions;
};

enum SET_MISA_EXTENSIONS_ERROR {
  SET_MISA_EXTENSIONS_SUCCESS = 0x00000,
  EMBEDDED_BASE_ISA_READ_ONLY = 0x00001,
  A_EXTENSION_MODIFICATION_DISALLOWED = 0x00002,
  B_EXTENSION_MODIFICATION_DISALLOWED = 0x00004,
  C_EXTENSION_MODIFICATION_DISALLOWED = 0x00008,
  D_EXTENSION_MODIFICATION_DISALLOWED = 0x00010,
  F_EXTENSION_MODIFICATION_DISALLOWED = 0x00020,
  H_EXTENSION_MODIFICATION_DISALLOWED = 0x00040,
  BASE_ISA_MODIFICATION_DISALLOWED = 0x00080,
  M_EXTENSION_MODIFICATION_DISALLOWED = 0x00100,
  Q_EXTENSION_MODIFICATION_DISALLOWED = 0x00200,
  S_MODE_MODIFICATION_DISALLOWED = 0x00400,
  U_MODE_MODIFICATION_DISALLOWED = 0x00800,
  V_EXTENSION_MODIFICATION_DISALLOWED = 0x01000,
  NON_STANDARD_EXTENSIONS_MODIFICATION_DISALLOWED = 0x02000,
  D_EXTENSION_REQUIRES_F_EXTENSION = 0x04000,
  Q_EXTENSION_REQUIRES_D_EXTENSION = 0x08000,
  S_MODE_REQUIRES_U_MODE = 0x10000,
  DISALLOWED_COMBINATION = 0x20000,
  IALIGN_INCREASE_DISALLOWED = 0x40000
};

struct MISA get_misa();
enum SET_MISA_EXTENSIONS_ERROR set_misa_extensions(enum EXTENSIONS extensions);


#endif